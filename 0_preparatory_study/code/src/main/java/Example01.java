public class Example01 {
  public static void main(String[] args) {
    // 화면에 내용 출력하는 예제 <-- 주석은 "//" 사용

    // 내용 출력 후 줄 바뀜
    System.out.println("Hello Java World");

    // 내용만 출력하기, 줄 안 바뀜
    System.out.print("Back-end");
    System.out.print("Programmer");

    // 내용 없이 줄바꿈만 출력
    System.out.println();
  }
}