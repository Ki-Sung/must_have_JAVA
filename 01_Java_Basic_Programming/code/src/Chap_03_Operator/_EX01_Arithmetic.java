package Chap_03_Operator;

public class _EX01_Arithmetic {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 3;

        System.out.println("num1 + num2 = " + (num1 + num2));
        System.out.println("num1 - num2 = " + (num1 - num2));
        System.out.println("num1 * num2 = " + (num1 * num2));
        System.out.println("num1 / num2 = " + (num1 / num2));       // 1번 - 나누기
        System.out.println("num1 % num2 = " + (num1 % num2));       // 2번 - 나머지
    }
}
